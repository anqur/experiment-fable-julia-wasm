# Build the web demo artifacts: lexer.wasm + lexer_meta.js (host-constant
# names, token-kind names/classes) + expected token streams for the node test.
using WasmCodegen, JuliaSyntax
include(joinpath(@__DIR__, "lexdemo.jl"))

webdir = normpath(joinpath(@__DIR__, "..", "web"))
mkpath(webdir)

comp = compile_wasm(lex_into, Tuple{String})
write(joinpath(webdir, "lexer.wasm"), comp.bytes)
println("lexer.wasm: ", length(comp.bytes), " bytes, ",
        length(comp.offloads), " imports, ", length(comp.hostconsts), " consts")

# token kind metadata: id -> (name, css class)
function kind_class(k)
    JuliaSyntax.is_error(k) && return "tok-error"
    JuliaSyntax.is_keyword(k) && return "tok-keyword"
    JuliaSyntax.is_operator(k) && return "tok-operator"
    name = string(k)
    name == "Identifier" && return "tok-identifier"
    name in ("Integer", "Float", "HexInt", "OctInt", "BinInt", "Float32") &&
        return "tok-number"
    occursin("String", name) && return "tok-string"
    name in ("Char", "CmdString", "Cmd") && return "tok-string"
    name == "Comment" && return "tok-comment"
    name in ("Whitespace", "NewlineWs") && return "tok-ws"
    name == "Bool" && return "tok-keyword"
    name == "@" && return "tok-macro"
    name == "MacroName" && return "tok-macro"
    return "tok-punct"
end

kinds = Dict{Int,Tuple{String,String}}()
for i in 0:2000
    k = try
        reinterpret(JuliaSyntax.Kind, UInt16(i))
    catch
        continue
    end
    s = try
        string(k)
    catch
        continue
    end
    isempty(s) && continue
    kinds[i] = (s, kind_class(k))
end

function js_str(s::AbstractString)
    buf = IOBuffer()
    print(buf, '"')
    for c in s
        c == '"' ? print(buf, "\\\"") :
        c == '\\' ? print(buf, "\\\\") :
        c == '\n' ? print(buf, "\\n") :
        c < ' ' ? print(buf, "\\u", lpad(string(UInt16(c), base=16), 4, '0')) :
        print(buf, c)
    end
    print(buf, '"')
    return String(take!(buf))
end

open(joinpath(webdir, "lexer_meta.js"), "w") do io
    println(io, "// generated by examples/lexer/build_web.jl")
    println(io, "export const HOSTCONSTS = [")
    for (name, v) in comp.hostconsts
        kind = v isa String ? "string" : v isa Symbol ? "symbol" : "opaque"
        val = v isa String ? js_str(v) : v isa Symbol ? js_str(String(v)) : "null"
        println(io, "  [", repr(name), ", ", repr(kind), ", ", val, "],")
    end
    println(io, "];")
    println(io, "export const IMPORTS = [",
            join((repr(name) for (_, name, _, _, _) in offload_imports(comp)), ", "), "];")
    println(io, "export const KINDS = {")
    for i in sort(collect(keys(kinds)))
        nm, cls = kinds[i]
        println(io, "  ", i, ": [", repr(nm), ", ", repr(cls), "],")
    end
    println(io, "};")
end

# expected tokens for the node-side differential check
corpus = [
    "x = 1 + foo(y)",
    "function f(a::Int, b)\n    return a * b - 2.5e3\nend",
    "s = \"str \$interp\" # comment\nwhile α₁ ≤ β && p .|> q; end",
    "0x1f 'c' `cmd` :sym [1,2]' @mac a..b",
]
function json_str(s::String)
    buf = IOBuffer()
    print(buf, '"')
    for c in s
        if c == '"'
            print(buf, "\\\"")
        elseif c == '\\'
            print(buf, "\\\\")
        elseif c == '\n'
            print(buf, "\\n")
        elseif c == '\t'
            print(buf, "\\t")
        elseif c < ' '
            print(buf, "\\u", lpad(string(UInt16(c), base=16), 4, '0'))
        else
            print(buf, c)
        end
    end
    print(buf, '"')
    return String(take!(buf))
end

open(joinpath(webdir, "expected_tokens.json"), "w") do io
    entries = ["{\"src\": $(json_str(src)), \"tokens\": [" *
               join(("[$k,$a,$b]" for (k, a, b) in native_tokens(src)), ",") * "]}"
               for src in corpus]
    println(io, "[", join(entries, ",\n"), "]")
end
println("wrote lexer_meta.js + expected_tokens.json")
