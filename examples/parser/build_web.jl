webdir = normpath(joinpath(@__DIR__, "..", "web"))
mkpath(webdir)

comp = compile_wasm(parse_into, Tuple{String,Int64})
write(joinpath(webdir, "parser.wasm"), comp.bytes)
println("parser.wasm: ", length(comp.bytes), " bytes, ",
