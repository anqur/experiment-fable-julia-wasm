# kind metadata: id -> (name, css class); used for both tokens and tree nodes
function kind_class(k)
    JuliaSyntax.is_error(k) && return "tok-error"
    JuliaSyntax.is_keyword(k) && return "tok-keyword"
    JuliaSyntax.is_operator(k) && return "tok-operator"
    name = string(k)
    name == "Identifier" && return "tok-identifier"
    name in ("Integer", "Float", "HexInt", "OctInt", "BinInt", "Float32") &&
        return "tok-number"
    occursin("String", name) && return "tok-string"
    name in ("Char", "CmdString", "Cmd") && return "tok-string"
    name == "Comment" && return "tok-comment"
    name in ("Whitespace", "NewlineWs") && return "tok-ws"
    name == "Bool" && return "tok-keyword"
    name == "@" && return "tok-macro"
    name == "MacroName" && return "tok-macro"
    return "tok-punct"
end

kinds = Dict{Int,Tuple{String,String}}()
for i in 0:2000
    k = try
        reinterpret(JuliaSyntax.Kind, UInt16(i))
    catch
        continue
    end
    s = try
        string(k)
    catch
        continue
    end
    isempty(s) && continue
    kinds[i] = (s, kind_class(k))
end

function js_str(s::AbstractString)
    buf = IOBuffer()
    print(buf, '"')
    for c in s
        c == '"' ? print(buf, "\\\"") :
        c == '\\' ? print(buf, "\\\\") :
        c == '\n' ? print(buf, "\\n") :
        c < ' ' ? print(buf, "\\u", lpad(string(UInt16(c), base=16), 4, '0')) :
        print(buf, c)
    end
    print(buf, '"')
    return String(take!(buf))
end

constkinds = Dict{String,Int}()
open(joinpath(webdir, "parser_meta.js"), "w") do io
    println(io, "// generated by examples/parser/build_web.jl")
    println(io, "export const HOSTCONSTS = [")
    for (name, v) in comp.hostconsts
        kind = v isa String ? "string" : v isa Symbol ? "symbol" : "opaque"
        constkinds[kind] = get(constkinds, kind, 0) + 1
        val = v isa String ? js_str(v) : v isa Symbol ? js_str(String(v)) : "null"
        println(io, "  [", repr(name), ", ", repr(kind), ", ", val, "],")
    end
    println(io, "];")
    println(io, "export const IMPORTS = [",
            join((repr(name) for (_, name, _, _, _) in offload_imports(comp)), ", "), "];")
    println(io, "export const KINDS = {")
    for i in sort(collect(keys(kinds)))
        nm, cls = kinds[i]
        println(io, "  ", i, ": [", repr(nm), ", ", repr(cls), "],")
    end
    println(io, "};")
end
println("const kinds: ", constkinds)

# expected event streams for the node-side differential check
corpus = [
    "x = 1 + foo(y)",
    "function f(a::Int, b)\n    return a * b - 2.5e3\nend",
    "struct Point{T<:Real}\n  x::T\n  y::T\nend",
    "try\n  risky()\ncatch e\n  rethrow()\nfinally\n  close(io)\nend",
    "[a[1] for a in xs if !isempty(a)]",
    "if true\n  0x1.8p3 + 1e1000 + 1f-50\nend",
    "wrong = 1.2.3 .+ 0xg \"unterminated",
    "\"\\q bad escape\" '\\q'",
    "x = 1e310",
]
function json_str(s::String)
    buf = IOBuffer()
    print(buf, '"')
    for c in s
        if c == '"'
            print(buf, "\\\"")
        elseif c == '\\'
            print(buf, "\\\\")
        elseif c == '\n'
            print(buf, "\\n")
        elseif c == '\t'
            print(buf, "\\t")
        elseif c < ' '
            print(buf, "\\u", lpad(string(UInt16(c), base=16), 4, '0'))
        else
            print(buf, c)
        end
    end
    print(buf, '"')
    return String(take!(buf))
end

# version-sensitive entries pin specific syntax versions
vcases = [("import A as B", 5), ("import A as B", 6),
          ("public foo, bar", 5), ("public foo, bar", 11),
          ("module A\nend", 12), ("module A\nend", 14),
          ("typegroup struct A end end", 14)]
open(joinpath(webdir, "expected_events.json"), "w") do io
    entry(src, v) = begin
        evs = native_events(src, v)
        "{\"src\": $(json_str(src)), \"v\": $v, \"events\": [" *
            join(("[$a,$b,$c]" for (a, b, c) in evs), ",") * "]}"
    end
    entries = vcat([entry(src, 14) for src in corpus],
                   [entry(src, v) for (src, v) in vcases])
    println(io, "[", join(entries, ",\n"), "]")
end
println("wrote parser_meta.js + expected_events.json")
